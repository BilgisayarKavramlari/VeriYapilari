def dizgiBirlestirme(dizgi1, dizgi2):
    return dizgi1 + " " + dizgi2

kelime1 = input("İlk kelimeyi giriniz: ")
kelime2 = input("İkinci kelimeyi giriniz: ")

dizgiBirlestirme(kelime1, kelime2)

# OUTPUT:
# İlk kelimeyi giriniz: Bilgisayar
# İkinci kelimeyi giriniz: Kavramları
# >>> 'Bilgisayar Kavramları'
